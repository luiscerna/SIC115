    public LibroMayorDialog(java.awt.Frame parent, boolean modal) throws SQLException {
        super(parent, modal);
        initComponents();
        this.setLocationRelativeTo(null); //Esto es para que se quede en el centro
        control=new MayorizacionControl();
        DefaultTableModel modelo= (DefaultTableModel) this.tbMayor.getModel();
        String[][] mayores=control.obtenerCuentasMayores();
        
        if(mayores!=null){
            Object[] fila = new Object[modelo.getColumnCount()];
            int a=mayores.length,i=0;
            System.out.println(a);
            while(i<a){
                fila[0]=mayores[i][0];
                fila[1]=mayores[i][1];
                fila[2]=mayores[i][2];
                fila[3]=mayores[i][3];
                fila[4]=mayores[i][4];
                modelo.addRow(fila);
                i++;
            }
        }else this.lblMensaje.setText("No hay transacciones que mostrar en este Periodo");
        
    }

