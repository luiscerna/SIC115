    /**
     * @param codigo the codigo to set
     */
    public void setCodigo(String codigo) {
        
        try {
            this.codigo = codigo;
            control=new MayorizacionControl();
            DefaultTableModel modelo= (DefaultTableModel) this.tbDetalles.getModel();
            
            if(!this.codigo.isEmpty()){
                String[][] detalles=control.obtenerPartidas(codigo);
                Object[] fila = new Object[modelo.getColumnCount()];
                int a=detalles.length,i=0;
                System.out.println(a);
                while(i<a){
                    fila[0]=detalles[i][0];
                    fila[1]=detalles[i][1];
                    fila[2]=detalles[i][2];
                    fila[3]=detalles[i][3];
                    fila[4]=detalles[i][4];
                    fila[5]=detalles[i][5];
                    modelo.addRow(fila);
                    i++;
                }
            }
        } catch (SQLException ex) {
            Logger.getLogger(DetallesDeMayorDialog.class.getName()).log(Level.SEVERE, null, ex);
        }
    }


