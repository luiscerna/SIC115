    private void cbEmpleadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbEmpleadoActionPerformed
        // TODO add your handling code here:
        String item=this.cbEmpleado.getItemAt(this.cbEmpleado.getSelectedIndex());
        for(String[] empleado : this.listaEmpleados){
            if((empleado[0]+", "+empleado[1]).equals(item)){
                this.txtNombre2.setText(empleado[0]);
                this.txtApellidos2.setText(empleado[1]);
                this.setEmpleadoEditar(empleado[2]);
                int selectPuesto=0;
                for(int i=0;i<this.cbPuesto2.getItemCount();i++ ){
                    if(this.cbPuesto2.getItemAt(i).equals(empleado[3]))
                        this.cbPuesto2.setSelectedIndex(i);
                }
                
            }
        }
        this.btnActualizar.setEnabled(true);
        this.btnEliminar.setEnabled(true);
    }//GEN-LAST:event_cbEmpleadoActionPerformed

