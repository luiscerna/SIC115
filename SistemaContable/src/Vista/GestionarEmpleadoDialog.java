    /**
     *
     * @param combo
     * @param i
     */
    public void llenarComboBox(javax.swing.JComboBox combo,int i){
        if(i==0){
            String aux="";
            this.cbEmpleado.removeAllItems();
            for(String[] item : this.listaEmpleados){
                aux=item[0]+", "+item[1];
                
                combo.addItem(aux);
            }
        }else{
            String aux="";
            for(String[] item : this.listaPuestos){
                aux=item[0];
                combo.addItem(aux);
            }
        }
            
        combo.setSelectedIndex(0);
    }

