    private void btnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarActionPerformed
        // TODO add your handling code here:
        String item=this.cbPuesto.getItemAt(this.cbPuesto.getSelectedIndex());
        String p="";
        for(String[] puesto : this.listaPuestos){
            if(puesto[0].equals(item)){
                p=puesto[2];
            }
        }
        if(this.txtApellidos1.getText().isEmpty()||this.txtNombre.getText().isEmpty())
            JOptionPane.showMessageDialog(null, "No puede dejar valores en blanco.", "Campo Vacío",JOptionPane.OK_OPTION);
        else
            if(this.control.nuevoPuestoEmpleado(0,this.txtNombre.getText(),this.txtApellidos1.getText(),p)){
                JOptionPane.showMessageDialog(null, "El nuevo empleado fue registrado con éxito.", "Registro Exitoso",JOptionPane.OK_OPTION);
                this.txtApellidos1.setText("");
                this.txtNombre.setText("");
                this.listaEmpleados=control.obtenerEmpleados();                
                this.cbEmpleado.removeAllItems();
                this.llenarComboBox(this.cbEmpleado, 0);
            }
            else
                JOptionPane.showMessageDialog(null, "Ocurrio un error al ingresar el empleado nuevo, vuelva a intentarlo más tarde.", "Error",JOptionPane.OK_OPTION);
    }//GEN-LAST:event_btnIngresarActionPerformed

